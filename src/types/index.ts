// Type Definitions for Bonsai Timer Extension
//
// Purpose:
// - Provides TypeScript types and interfaces used throughout the application
// - Defines constants for the application
//
// Contains:
// - TimerState: Interface for timer state
// - BonsaiTree: Interface for a completed bonsai tree
// - BonsaiCollection: Type for collection of completed trees
// - TimerFormData: Interface for timer form input
// - BONSAI_STAGES: Constant array of ASCII art for different growth stages
// - DEFAULT_TIMER_STATE: Default state for the timer
//
// These types ensure type safety across the application and
// provide a single source of truth for shared data structures.
